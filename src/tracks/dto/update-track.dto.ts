import {
  IsString,
  IsOptional,
  IsInt,
  Min,
  MinLength,
  IsBoolean,
} from 'class-validator';

export class UpdateTrackDto {
  @IsOptional()
  @IsString()
  @MinLength(1)
  name?: string;

  @IsOptional()
  @IsString()
  description?: string;

  @IsOptional()
  @IsString()
  color?: string;

  @IsOptional()
  @IsInt()
  @Min(0)
  sortOrder?: number;

  @IsOptional()
  @IsBoolean()
  isActive?: boolean;
}
